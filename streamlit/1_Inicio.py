import streamlit as st

# Título
st.title('informacion del EPH')

# Descripcion de EPH
st.write('La Encuesta Permanente de Hogares (EPH) es un programa de relevamiento '
    'estadístico realizado por el INDEC en Argentina, que tiene como objetivo principal '
    'obtener información sobre las características socioeconómicas de la población. '
    'A través de entrevistas en hogares seleccionados, la EPH recoge datos sobre empleo, '
    'ingresos, educación, condiciones de vivienda y otros aspectos sociales relevantes. ' \
    'Esta información es fundamental para analizar la situación del mercado laboral y '
    'diseñar políticas públicas basadas en evidencia.')